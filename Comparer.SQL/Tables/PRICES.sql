CREATE TABLE [dbo].[PRICES] (
    [ID]              UNIQUEIDENTIFIER NOT NULL,
    [NAME]            VARCHAR (255)    NOT NULL,
    [DISID]           UNIQUEIDENTIFIER NULL,
    [DISCOUNT]        FLOAT (53)       NOT NULL,
    [DEFAULTCURRENCY] TINYINT          NOT NULL,
    [TURNDOLLARSRATE] FLOAT (53)       NOT NULL,
    [SHEET]           VARCHAR (1024)   NOT NULL,
    [NAMERANGE]       VARCHAR (16)     NOT NULL,
    [PRICERANGE]      VARCHAR (16)     NOT NULL,
    [FIRSTROW]        INT              NOT NULL,
    [MAXPRICECHANGE]  REAL             NOT NULL,
    [STOPWORDS]       VARCHAR (6000)   NULL,
    [FILENAME]        VARCHAR (1024)   NULL,
    [FILEDATE]        DATETIME         NULL,
    [FILESHEETS]      VARCHAR (1024)   NULL,
    [COMMENT]         NVARCHAR (1024)  DEFAULT ('') NOT NULL,
    [SKURANGE]        VARCHAR (16)     DEFAULT ('') NOT NULL,
    [STOCKRANGE]      NVARCHAR (16)    DEFAULT ('') NOT NULL,
    [INSTOCKRANGE]    NVARCHAR (16)    DEFAULT ('') NOT NULL,
    [ISACTIVE]        BIT              DEFAULT ((1)) NOT NULL,
    CONSTRAINT [PRICESPRIMARY] PRIMARY KEY CLUSTERED ([ID] ASC),
    CONSTRAINT [PRICESFOREIGNDISTRIBUTOR] FOREIGN KEY ([DISID]) REFERENCES [dbo].[DISTRIBUTORS] ([ID]) ON DELETE CASCADE ON UPDATE CASCADE,
    CONSTRAINT [PRICESUNIQUEID] UNIQUE NONCLUSTERED ([ID] ASC),
    CONSTRAINT [PRICESUNIQUENAME] UNIQUE NONCLUSTERED ([NAME] ASC)
);

