CREATE TABLE [dbo].[PRODUCTS] (
    [ID]              UNIQUEIDENTIFIER NOT NULL,
    [NAME]            VARCHAR (1024)   NOT NULL,
    [MANID]           UNIQUEIDENTIFIER NOT NULL,
    [PRICE]           FLOAT (53)       NOT NULL,
    [BASEPRICE]       FLOAT (53)       NULL,
    [LABEL]           VARCHAR (255)    NOT NULL,
    [PARENTLABEL]     VARCHAR (255)    NULL,
    [STATE]           TINYINT          NOT NULL,
    [USERCHANGED]     BIT              NOT NULL,
    [EXTRAUSED]       BIT              NOT NULL,
    [EXTRA]           INT              NOT NULL,
    [AUTOUPDATE]      BIT              NOT NULL,
    [AUTOUPDATETESTS] BIT              NULL,
    [PRODUCTEXISTS]   BIT              NULL,
    [PHOTOEXISTS]     BIT              DEFAULT ((0)) NOT NULL,
    [VIEWSTYLEID]     INT              DEFAULT ((1)) NOT NULL,
    [CHILDNAME]       NVARCHAR (1024)  DEFAULT ('') NOT NULL,
    [TESTER]          BIT              DEFAULT ((0)) NOT NULL,
    [PUBLISHED]       BIT              DEFAULT ((1)) NOT NULL,
    [CREATEDATE]      DATETIME         DEFAULT (getdate()) NOT NULL,
    [CHANGEDATE]      DATETIME         DEFAULT (getdate()) NOT NULL,
    [CORRECTEDSTATUS] TINYINT          DEFAULT ((0)) NOT NULL,
    [INSTOCK]         INT              DEFAULT ((10000)) NOT NULL,
    [ISPROBIRKA]      BIT              DEFAULT ((0)) NOT NULL,
    [COMMENT]         NVARCHAR (500)   DEFAULT ('') NOT NULL,
    [DELETED]         BIT              DEFAULT ((0)) NOT NULL,
    [IS_PROMO]        BIT              DEFAULT ((0)) NOT NULL,
    [TEMPLATEID]      INT              DEFAULT ((0)) NOT NULL,
    [SKU]             INT              DEFAULT ((0)) NOT NULL,
    [ISNEW]           BIT              DEFAULT ((0)) NOT NULL,
    [ISNEWSTARTDATE]  DATETIME         NULL,
    [WEIGHT]          FLOAT (53)       DEFAULT ((100)) NOT NULL,
    [DEFAULTCURRENCY] TINYINT          DEFAULT ((2)) NOT NULL,
    CONSTRAINT [PRODUCTSPRIMARY] PRIMARY KEY CLUSTERED ([ID] ASC),
    CONSTRAINT [PRODUCTSFOREIGNMANUFACTURER] FOREIGN KEY ([MANID]) REFERENCES [dbo].[MANUFACTURERS] ([ID]) ON DELETE CASCADE ON UPDATE CASCADE,
    CONSTRAINT [PRODUCTSUNIQUEID] UNIQUE NONCLUSTERED ([ID] ASC),
    CONSTRAINT [PRODUCTSUNIQUELABEL] UNIQUE NONCLUSTERED ([LABEL] ASC),
    CONSTRAINT [PRODUCTSUNIQUESKU] UNIQUE NONCLUSTERED ([SKU] ASC)
);

